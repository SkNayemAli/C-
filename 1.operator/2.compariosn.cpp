#include<iostream>
using namespace std;       //if condition is true then  output will be -> 1. otherwise-> 0.
int main()
{
int a=4,b=5;
cout<<"The value of a==b is: "<<(a==b)<<endl;     //if equal then output will be -> 1. ifnot equal then output will be->0.
cout<<"The value of a!=b is: "<<(a!=b)<<endl;
cout<<"The value of a>=b is: "<<(a>=b)<<endl;
cout<<"The value of a<=b is: "<<(a<=b)<<endl;
cout<<"The value of a>b is: "<<(a>b)<<endl;
cout<<"The value of a<b is: "<<(a<b)<<endl;
}
